
public class Clasificacion {

  public void clasificarNum(int num) {
    if (num == 0) {
      System.out.println("El número ingresado es 0.");
    } else if (num < 0) {
      System.out.println("El número ingresado es un número negativo.");
    } else {
      System.out.println("El número ingresado es un número positivo.");
    }

  }
}
